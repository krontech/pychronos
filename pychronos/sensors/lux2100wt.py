class lux2100wavetab:
    def __init__(self, wavetab, abnDelay=25, clocks=0):
        self.wavetab = wavetab
        self.abnDelay = abnDelay
        if (clocks == 0):
            ## Guess the wavetable length from the size of the wavetable
            tail = -1
            while bytearray[tail] == 0:
                tail = tail-1
            self.clocks = (len(wavetab) + tail + 5) // 6
        else:
            self.clocks = clocks

